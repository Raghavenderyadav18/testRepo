package jav.operators;

public class AssignmentOperators
{

	public static void main(String[] args)
	{
         int a=8;
         System.out.println(a+=1);
         int b=5;
         System.out.println(b-=2);
         int c=6;
         System.out.println(c*=2);
         int d=9;
         System.out.println(d/=3);
         int e=11;
         System.out.println(e%=5);
	}

}
