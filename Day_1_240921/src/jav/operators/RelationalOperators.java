package jav.operators;

public class RelationalOperators 
{

	public static void main(String[] args) 
	{
           int num1=5;
           int num2=6;
           
           System.out.println("num1<num2="+(num1<num2)); //lessthan
           
           System.out.println("num1>num2="+(num1>num2)); //greaterthan
           
           System.out.println("num1<=num2="+(num1<=num2)); //lessthan or equalto
           
           System.out.println("num1>=num2="+(num1>=num2)); // greaterthan or equalto
           
           System.out.println("num1==num2="+(num1==num2)); // double equalto
           
           System.out.println("num1!=num2="+(num1!=num2)); //not equalto
	}

}
